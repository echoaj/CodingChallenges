

def mpsr(i , j):
    if i < h and j < w:
        return grid[i][j] + min(mpsr(i+1, j), mpsr(i, j+1))
    elif i < h:
        return grid[i][j] + mpsr(i+1, j)
    elif j < w:
        return grid[i][j] + mpsr(i, j+1)
    else:
        return grid[i][j]





def mpst(grid):
    w = len(grid[0])
    h = len(grid)
    table = [[0] * w] * h

    for i in range(h):
        for j in range(w):
            if i > 0 and j > 0:
                table[i][j] = grid[i][j] + min(table[i-1][j], table[i][j-1])
            elif i > 0:
                table[i][j] = grid[i][j] + table[i-1][j]
            elif j > 0:
                table[i][j] = grid[i][j] + table[i][j-1]
            else:
                table[i][j] = grid[i][j]
    return table[h-1][w-1]


